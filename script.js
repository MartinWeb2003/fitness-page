const LANG_KEY = 'bmfit-lang';
let currentLang = 'hr';

function setLang(lang) {
  if (!T[lang]) return;
  currentLang = lang;
  document.documentElement.lang = lang;
  try { localStorage.setItem(LANG_KEY, lang); } catch (e) { /* private mode */ }

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  const t = T[lang];

  // text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // innerHTML (contains tags)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // placeholders
  document.querySelectorAll('[data-placeholder-i18n]').forEach(el => {
    const key = el.getAttribute('data-placeholder-i18n');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // image alt text
  document.querySelectorAll('[data-alt-i18n]').forEach(el => {
    const key = el.getAttribute('data-alt-i18n');
    if (t[key] !== undefined) el.alt = t[key];
  });

  // page title — each page tags itself via <body data-page="...">
  const page = document.body.dataset.page;
  if (TITLES[page]) document.title = TITLES[page][lang];
}

// init — restore the visitor's choice across pages, default Croatian
