// ── PARALLAX
// Backdrop layers travel further than the page scrolls, so they read as moving
// faster than the content in front of them. data-parallax is the amplitude in
// px: each layer runs from +amp to -amp across the section's pass.
const parallaxEls = document.querySelectorAll('[data-parallax]');
if (parallaxEls.length && !window.matchMedia('(prefers-reduced-motion: reduce)').matches) {
  let ticking = false;
  const place = () => {
    parallaxEls.forEach(el => {
      const host = el.closest('section');
      const r = host.getBoundingClientRect();
      // 0 as the section's top edge reaches the bottom of the viewport,
      // 1 as its bottom edge leaves the top.
      const p = Math.min(Math.max((window.innerHeight - r.top) / (r.height + window.innerHeight), 0), 1);
      const amp = parseFloat(el.dataset.parallax);
      el.style.transform = 'translate3d(0,' + (-(p - 0.5) * 2 * amp).toFixed(1) + 'px,0)';
    });
    ticking = false;
  };
  const onScroll = () => { if (!ticking) { ticking = true; requestAnimationFrame(place); } };
  window.addEventListener('scroll', onScroll, { passive: true });
  window.addEventListener('resize', onScroll);
  place();
}

